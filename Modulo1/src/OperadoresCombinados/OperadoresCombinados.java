package OperadoresCombinados;

public class OperadoresCombinados {

    public static void main(String[] args) {
        int a = 5;
        int b = 2;
        b += a;
        System.out.println(b);

        int c = 5;
        int d = 2;
        d -= c;
        System.out.println(d);

        int e = 5;
        int f = 2;
        f *= e;
        System.out.println(f);

        int g = 5;
        double h = 2;
        h /= g;
        System.out.println(h);

        int i = 5;
        int j = 2;
        j %= i;
        System.out.println(j);
    }
}
